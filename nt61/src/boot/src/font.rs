    /// Get font data for a character (ASCII 0x20-0x7E)
    pub fn get_char(c: u8) -> Option<&'static [u8; 16]> {
        if c < 0x20 || c > 0x7E {
            return None;
        }
        let idx = ((c - 0x20) as usize) * 16;
        if idx + 16 <= FONT_DATA.len() {
            Some(unsafe {
                core::slice::from_raw_parts(
                    FONT_DATA.as_ptr().add(idx),
                    16
                ).try_into().unwrap_unchecked()
            })
        } else {
            None
        }
    }

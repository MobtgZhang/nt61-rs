/// Built-in bitmap font
pub struct BitmapFont {
    scale: u32,
}

impl BitmapFont {
    /// Zero-width character data (space)
    const ZERO_CHAR: [u8; 16] = [0; 16];

    pub fn new() -> Self {
        Self { scale: 1 }
    }

    /// Set the font scale (1 = normal, 2 = double size, etc.)
    pub fn set_size(&mut self, size: u32) {
        // Base font is 16 pixels tall, so scale = size / 16
        self.scale = (size / 16).max(1);
    }

    /// Get the current character height accounting for scale
    pub fn char_height(&self) -> u32 {
        16 * self.scale
    }

    /// Get the line height (same as char_height for bitmap font)
    pub fn line_height(&self) -> u32 {
        self.char_height()
    }

    /// Measure a string - returns (width, height)
    pub fn measure(&self, text: &str) -> (u32, u32) {
        measure_string(self, text)
    }

    /// Get the current character width accounting for scale
    pub fn char_width(&self) -> u32 {
        8 * self.scale
    }
    
    /// Get glyph for a character. Returns None for unsupported characters.
    /// No fallback - unknown characters simply don't render (prevents garbage).
    pub fn get_char(&self, c: char) -> Option<BitmapGlyph<'_>> {
        let code = c as u32;

        // Space character
        if code == 0x20 {
            return Some(BitmapGlyph { data: &Self::ZERO_CHAR });
        }

        // ASCII printable range (0x21-0x7E)
        if code >= 0x21 && code <= 0x7E {
            return bitmap::get_char(code as u8).map(|data| BitmapGlyph { data });
        }

        // Tab -> space
        if code == 0x09 {
            return Some(BitmapGlyph { data: &Self::ZERO_CHAR });
        }

        // No fallback for other characters - return None to prevent garbage
        None
    }

    /// Draw text at position
    pub fn draw_text(&mut self, fb: &mut Framebuffer, text: &str,
                     x: i32, y: i32, fg: Color, bg: Option<Color>) {
        draw_text(fb, self, text, x, y, fg, bg);
    }

    /// Draw centered text
    pub fn draw_text_centered(&mut self, fb: &mut Framebuffer, text: &str,
                              center_x: i32, y: i32, fg: Color, bg: Option<Color>) {
        let (width, _) = self.measure(text);
        let x = center_x - (width as i32) / 2;
        self.draw_text(fb, text, x, y, fg, bg);
    }
}

impl Default for BitmapFont {
    fn default() -> Self {
        Self::new()
    }
}

/// Bitmap glyph for rendering
pub struct BitmapGlyph<'a> {
    pub data: &'a [u8; 16],
}

impl BitmapGlyph<'_> {
    pub fn width(&self) -> u32 { 8 }
    pub fn height(&self) -> u32 { 16 }

    /// Draw the glyph at position (x, y) with foreground and background colors
    /// Font data format: bit 7 = leftmost pixel, bit 0 = rightmost pixel (standard)
    pub fn draw(&self, fb: &mut Framebuffer, x: i32, y: i32, fg: Color, bg: Option<Color>) {
        self.draw_scaled(fb, x, y, fg, bg, 1);
    }

    /// Draw the glyph at position (x, y) with scaling
    pub fn draw_scaled(&self, fb: &mut Framebuffer, x: i32, y: i32, fg: Color, bg: Option<Color>, scale: u32) {
        let char_h = 16 * scale as i32;
        let char_w = 8 * scale as i32;

        // Draw background first if specified
        if let Some(bg_color) = bg {
            for dy in 0..char_h {
                for dx in 0..char_w {
                    fb.set_pixel((x + dx) as u32, (y + dy) as u32, bg_color);
                }
            }
        }

        // Draw scaled bitmap
        for row in 0..16i32 {
            let byte = self.data[row as usize];
            for col in 0..8i32 {
                let bit = 7 - col; // Bit 7 = leftmost
                if (byte >> bit) & 1 == 1 {
                    // Draw scaled pixel block
                    for sy in 0..scale as i32 {
                        for sx in 0..scale as i32 {
                            fb.set_pixel((x + col * scale as i32 + sx) as u32,
                                        (y + row * scale as i32 + sy) as u32,
                                        fg);
                        }
                    }
                }
            }
        }
    }
}

/// Draw text using bitmap font with proper background clearing
pub fn draw_text(fb: &mut Framebuffer, font: &BitmapFont, text: &str,
                  x: i32, y: i32, fg: Color, bg: Option<Color>) {
    let mut cursor_x = x;
    let mut cursor_y = y;
    let char_h = font.char_height() as i32;
    let char_w = font.char_width() as i32;
    let tab_width = char_w * 4; // Tab = 4 spaces

    for c in text.chars() {
        match c {
            '\n' => {
                cursor_x = x;
                cursor_y += char_h;
            }
            '\r' => {
                // Skip carriage return
            }
            '\t' => {
                // Tab = advance to next tab stop
                let offset = cursor_x - x;
                let next_tab = ((offset / tab_width) + 1) * tab_width;
                cursor_x = x + next_tab;
            }
            ' ' => {
                // Draw space with background color if specified
                if let Some(bg_color) = bg {
                    for dy in 0..char_h {
                        for dx in 0..char_w {
                            fb.set_pixel((cursor_x + dx) as u32, (cursor_y + dy) as u32, bg_color);
                        }
                    }
                }
                cursor_x += char_w;
            }
            _ => {
                if let Some(glyph) = font.get_char(c) {
                    glyph.draw_scaled(fb, cursor_x, cursor_y, fg, bg, font.scale);
                    cursor_x += char_w;
                }
            }
        }
    }
}

/// Measure string dimensions
pub fn measure_string(font: &BitmapFont, text: &str) -> (u32, u32) {
    let char_w = font.char_width();
    let char_h = font.char_height();
    let tab_width = char_w * 4; // Tab = 4 spaces
    let mut width = 0u32;
    let mut max_width = 0u32;
    let mut height = char_h;
    let mut cursor_x = 0u32;

    for c in text.chars() {
        match c {
            '\n' => {
                if width > max_width {
                    max_width = width;
                }
                height += char_h;
                width = 0;
                cursor_x = 0;
            }
            '\t' => {
                // Tab = advance to next tab stop
                let next_tab = ((cursor_x / tab_width) + 1) * tab_width;
                cursor_x = next_tab;
                width = cursor_x;
            }
            ' ' => {
                cursor_x += char_w;
                width = cursor_x;
            }
            _ => {
                // Count known characters (ASCII printable or mapped special chars)
                let c = c as u32;
                if c == 0x20 || c == 0x09 || c == 0xA0 || // space, tab, NBSP
                   (c >= 0x21 && c <= 0x7E) || // ASCII printable
                   matches!(c, 0x2018..=0x201D | 0x2013..=0x2014 | 0x2026) {
                    cursor_x += char_w;
                    width = cursor_x;
                }
            }
        }
    }

    if width > max_width {
        max_width = width;
    }

    (max_width, height)
}

/// Draw centered text
pub fn draw_text_centered(fb: &mut Framebuffer, font: &BitmapFont, text: &str,
                          center_x: i32, y: i32, fg: Color, bg: Option<Color>) {
    let (width, _) = measure_string(font, text);
    let x = center_x - (width as i32) / 2;
    draw_text(fb, font, text, x, y, fg, bg);
}

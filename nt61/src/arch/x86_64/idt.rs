/// Set the gate for `vector` to the supplied handler.
/// Uses selector 0x38 (OVMF slot 7 — the 64-bit kernel CS). Slot 2
/// (0x10) is only the legacy 32-bit code segment and is **invalid**
/// as a target CS for an interrupt gate in long mode (the CPU faults
/// during delivery).
pub fn set_gate(vector: u8, handler: u64, ist: u8, dpl: u8) {
    unsafe {
        let entry = &mut IDT[vector as usize];
        *entry = GateDescriptor::interrupt(handler, crate::arch::x86_64::gdt::KERNEL_CS, ist, dpl);
    }
}


        let idt_base = &IDT as *const GateDescriptor;
        let idt_page_start = idt_base as u64;
        let idt_page_end = idt_page_start + core::mem::size_of_val(&IDT) as u64;
        // Touch the first byte of each 4 KiB page that IDT spans.

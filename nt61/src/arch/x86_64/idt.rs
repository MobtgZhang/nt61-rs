//! Interrupt Descriptor Table (IDT)
//
//! 256-entry IDT for the x86_64 exception and interrupt vectors.
//! The first 32 are CPU-defined exceptions; the rest are user-
//! defined IRQ handlers, dispatched through the APIC or the legacy
//! 8259 PIC.
//
//! Each entry uses the `GateDescriptor` format (16 bytes): handler
//! address, segment selector (always `KERNEL_CS`), IST field, type
//! and DPL, plus the upper 32 bits of the handler address.

use core::arch::asm;

#[repr(C, packed)]
#[derive(Clone, Copy, Default)]
pub struct GateDescriptor {
    pub offset_low: u16,
    pub selector: u16,
    pub ist: u8,
    pub type_attr: u8,
    pub offset_mid: u16,
    pub offset_high: u32,
    pub _reserved: u32,
}

impl GateDescriptor {
    pub const fn empty() -> Self {
        Self { offset_low: 0, selector: 0, ist: 0, type_attr: 0,
               offset_mid: 0, offset_high: 0, _reserved: 0 }
    }
    /// Build a 64-bit interrupt gate.
    pub fn interrupt(handler: u64, sel: u16, ist: u8, dpl: u8) -> Self {
        Self {
            offset_low: (handler & 0xFFFF) as u16,
            selector: sel,
            ist: ist & 0x7,
            type_attr: 0x8E | ((dpl & 0x3) << 5),
            offset_mid: ((handler >> 16) & 0xFFFF) as u16,
            offset_high: ((handler >> 32) & 0xFFFF_FFFF) as u32,
            _reserved: 0,
        }
    }
}

